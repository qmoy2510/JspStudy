package vo;

public class RentalVO {
	private String rentYmd;
	private String rentNo;
	private String rentBook;
	private int rentRent;
	private String closeYmd;
	private String returnYmd;
	private String returnFg;
	public String getRentYmd() {
		return rentYmd;
	}
	public void setRentYmd(String rentYmd) {
		this.rentYmd = rentYmd;
	}
	public String getRentNo() {
		return rentNo;
	}
	public void setRentNo(String rentNo) {
		this.rentNo = rentNo;
	}
	public String getRentBook() {
		return rentBook;
	}
	public void setRentBook(String rentBook) {
		this.rentBook = rentBook;
	}
	public int getRentRent() {
		return rentRent;
	}
	public void setRentRent(int rentRent) {
		this.rentRent = rentRent;
	}
	public String getCloseYmd() {
		return closeYmd;
	}
	public void setCloseYmd(String closeYmd) {
		this.closeYmd = closeYmd;
	}
	public String getReturnYmd() {
		return returnYmd;
	}
	public void setReturnYmd(String returnYmd) {
		this.returnYmd = returnYmd;
	}
	public String getReturnFg() {
		return returnFg;
	}
	public void setReturnFg(String returnFg) {
		this.returnFg = returnFg;
	}
	
}
